#include <bits/stdc++.h>

using namespace std;

struct ListNode
{
    int val;
    ListNode *next;
    ListNode(int x) : val(x), next(NULL) {}
};

class Solution
{
public:
    bool hasCycle(ListNode *head)
    {
        // so we will use two pointer approach
        // slow and fast pointer, the fast pointer move by two units whereas the slow pointer moves by one unit
        // if fast pointer reached null then there is no cycles in the list
        // if the fast pointer the slow pointer collide then there is a cycle in the list
        ListNode *slow = head;
        ListNode *fast = head;
        while (fast && fast->next)
        {
            slow = slow->next;
            fast = fast->next->next;
            if (slow == fast)
            {
                return true;
            }
        }
        return false;
    }
};